
public class Main {
    public static void main(String[] args) {
        // structure boucle for => exécute un bloc de code limité à un nombre d'itération

        //for( [initialiser la variable] ; [nombre d'itérations] ;
        // [incrémentation de la variable à chaque itération] )
        for(int index = 0; index<=10; index++) {
            System.out.println(index);
        }

        System.out.println("Fin !");
    }
}